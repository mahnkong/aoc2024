use strict;
use Data::Dumper;

my $rules = {
    'N' => {
        move => sub ($$) {
            my $i = shift;
            my $j = shift;
            return $i-1,$j;
        },
        next => 'E',

    },
    'S' => {
        move => sub ($$) {
            my $i = shift;
            my $j = shift;
            return ($i+1,$j);
        },
        next => 'W'
    },
    'W' => {
        move => sub ($$) {
            my $i = shift;
            my $j = shift;
            return ($i,$j-1);
        },
        next => 'N'
    },
    'E' => {
        move => sub ($$) {
            my $i = shift;
            my $j = shift;
            return ($i,$j+1);
        },
        next => 'S'
    }
};

my $current_direction;
my $last_coordinates = [undef, undef];
my $current_coordinates = [undef, undef];
my $map = [];

my $max_i = 0;
my $max_j = 0;

my $i = 0;
foreach my $line (<DATA>) {
    chomp $line;
    my $j = 0;
    $map->[$i] = [];
    foreach my $p (split //, $line) {
        if ($p eq '^') {
            $current_direction = 'N';
            $current_coordinates = [$i, $j];
            $p = '.';
        }
        $map->[$i]->[$j] = $p;
        $j += 1;
    }
    $i += 1;
    $max_j = $j-1 unless $max_j;
}
$max_i = $i-1;

my %positions;
do {
    $positions{$current_coordinates->[0]."X".$current_coordinates->[1]} = undef;
    my ($next_i, $next_j) = $rules->{$current_direction}->{move}->($current_coordinates->[0], $current_coordinates->[1]);
    if ($map->[$next_i]->[$next_j] eq "#") {
        $current_direction = $rules->{$current_direction}->{next};
        print "-> $current_direction\n";
    }
    @$current_coordinates = $rules->{$current_direction}->{move}->($current_coordinates->[0], $current_coordinates->[1]);
    print "$current_coordinates->[0]:$current_coordinates->[1] -> " . $map->[$current_coordinates->[0]]->[$current_coordinates->[1]] . "\n";
} while (cordinates_valid($current_coordinates));

print "Positions: " . scalar(keys(%positions))."\n";

sub cordinates_valid($) {
    my $coords = shift;

    return 0 if $coords->[0] < 0 || $coords->[0] > $max_i;
    return 0 if $coords->[1] < 0 || $coords->[1] > $max_j;
    return 1;
}


